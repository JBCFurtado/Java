package comentarios;

/**
 * Essa classe demonstra a utilização de variáveis em uma classe em java.
 * São declaradas variaveis do tipo int e double.
 * O exemplo também demonstra como imprimir o conteúdo das variáveis na tela.
 * @author Jobs
 */

public class Comentarios {
	public static void main (String args []) {
		int matricula = 1089, idade = 20; // Declaracão de variáveis do tipo inteiro.
		double dolar = 2.62; // Declaracão de variáveis do tipo Double.
		//A linhas seguintes enviam o conteúdo das váriaveis para a tela.
		System.out.println("Matricula: "+ matricula + "idade: " + idade +"dolar"+ dolar);
	}
}
